const User = require("../models/user.model.js");




// Create users table in database
exports.createTable = (req, res) => {
  User.createTable((err, data) => {
    if (err)
      res.status(500).send({
        message:
          err.message || "Some error occurred while retrieving users."
      });
    else res.send(data);
  });
};



